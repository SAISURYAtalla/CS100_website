					<p>The Gornergrat (3,089 m) is one of those places that everyone should visit at least once in a lifetime. The mountain panorama and the glorious view of the Matterhorn are unforgettable. With its sunny observation platform accessible all year, the Gornergrat, at an altitude of 3,089 m, has been one of Switzerland’s top excursion destinations since 1898. The Gornergrat Bahn was the world’s first fully electrified cog railway. Today it is a modern, eco-friendly railway, equipped with a regenerative braking system that generates electricity on the descent and so saves energy.</p>
